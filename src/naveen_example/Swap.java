package naveen_example;

public class Swap {

	public static void main(String[] args) {
		int a=3;
		int b=4;
		System.out.println("value before swapping = " );
		System.out.println("a="+a);
		System.out.println("b="+b);
		int c;
		c=a;
		a=b;
		b=c;
		System.out.println("value after swapping =");
		System.out.println("a="+a);
		System.out.println("b="+b);


	}

}
